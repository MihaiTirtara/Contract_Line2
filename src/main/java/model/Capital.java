package model;

public class Capital 
{
	private int share;
	private int value;
	
	
	public Capital(int share, int value) 
	{
		this.share = share;
		this.value = value;
	}


	public int getShare() {
		return share;
	}


	public void setShare(int share) {
		this.share = share;
	}


	public int getValue() {
		return value;
	}


	public void setValue(int value) {
		this.value = value;
	}
	
	

}
